<?PHP

$HOSTNAME='LOCALHOST';
$DATABASE='APSEGUROS';
$USERNAME='ROOT';
$PASSWORD='';

TRY{
    $CON = NEW PDO("MYSQL:HOST=$HOSTNAME;DBNAME=DATABASE",$USERNAME,$PASSWORD)
} CATCH(PDOEXCEPTION $E){
